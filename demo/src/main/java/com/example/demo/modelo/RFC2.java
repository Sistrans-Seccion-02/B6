package com.example.demo.modelo;

public class RFC2 {
    private int habitacionNumero;
    private Float indiceDeOcupacion;
    public RFC2() {
        super();
    }
    public RFC2(int habitacionNumero, Float indiceDeOcupacion) {
        super();
        this.habitacionNumero = habitacionNumero;
        this.indiceDeOcupacion = indiceDeOcupacion;
    }
    public int getHabitacionNumero() {
        return habitacionNumero;
    }
    public void setHabitacionNumero(int habitacionNumero) {
        this.habitacionNumero = habitacionNumero;
    }
    public Float getIndiceDeOcupacion() {
        return indiceDeOcupacion;
    }
    public void setIndiceDeOcupacion(Float indiceDeOcupacion) {
        this.indiceDeOcupacion = indiceDeOcupacion;
    }

    
    
}
